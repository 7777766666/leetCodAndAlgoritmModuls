package org.example.myTest;

public class Tiger {

    private int mass;

    private String name;

    public Tiger(int massTiger, String nameTiger){
        this.mass = massTiger;
        this.name = nameTiger;
    }

    public String getTigerName(){
        return name;
    }
    public int getTigerMass(){
        return mass;
    }



}
